function sumatr(a, b) {
    return a + b;
}

function restatr(a, b) {
    return a - b;
}

function multiplicaciontr(a, b) {
    return a * b;
}

function divisiontr(a, b) {
    return a / b;
}

console.log(sumatr(10,10));
console.log(restatr(10,10));
console.log(multiplicaciontr(10,10));
console.log(divisiontr(10,10));
